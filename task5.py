x = 2.5
y = -3.8

if x == 0 and y == 0:
    print("Точка знаходиться в початку координат.")
elif x == 0:
    print("Точка знаходиться на осі Y.")
elif y == 0:
    print("Точка знаходиться на осі X.")
elif x > 0 and y > 0:
    print("Точка знаходиться у правому верхньому куті.")
elif x < 0 and y > 0:
    print("Точка знаходиться у лівому верхньому куті.")
elif x < 0 and y < 0:
    print("Точка знаходиться у лівому нижньому куті.")
else:
    print("Точка знаходиться у правому нижньому куті.")
